	{echo "Mièrcoles";}

elseif ($dia=="Thu")
	{echo "Jueves";}

elseif ($dia=="Fri")
	{echo "Viernes";}

elseif ($dia=="Sat")
	{echo "Sàbado";}

elseif ($dia=="Sun")
	{echo "Domingo";}
